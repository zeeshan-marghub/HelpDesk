package com.zeeshan.HelpDesk.Entity;

public class Employee {
private int empID;
private String empName;
    //Getters
    public int getEmpID() {
        return empID;
    }

    public String getEmpName() {
        return empName;
    }
    //Setters
    public void setEmpID(int empID) {
        this.empID = empID;
    }

    public void setEmpName(String empName) {
        this.empName = empName;
    }
}
